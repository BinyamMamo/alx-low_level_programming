#include "3-calc.h"

/**
 * op_add - op add
 * @a: first input
 * @b: second input
 *
 * Return: int
 */
int op_add(int a, int b) { return (a + b); }
/**
 * op_sub - op sub
 * @a: first input
 * @b: second input
 *
 * Return: int
 */
int op_sub(int a, int b) { return (a - b); }
/**
 * op_mul - op mul
 * @a: first input
 * @b: second input
 *
 * Return: int
 */
int op_mul(int a, int b) { return (a * b); }
/**
 * op_div - op div
 * @a: first input
 * @b: second input
 *
 * Return: int
 */
int op_div(int a, int b) { return (a / b); }
/**
 * op_mod - op mod
 * @a: first input
 * @b: second input
 *
 * Return: int
 */
int op_mod(int a, int b) { return (a % b); }
